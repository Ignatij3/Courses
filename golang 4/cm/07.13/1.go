package main

import  (
	"fmt"
	"math"
)	
	
func main()  {
	var sum1, sum2, x float64
	N:= []float64{100.0, 1000.0, 10000.0, 100000.0, 1000000.0, 10000000.0, 100000000.0, 1000000000.0, 10000000000.0, 100000000000.0, 1000000000000.0, 10000000000000.0, 100000000000000.0, 1000000000000000.0, 100000000000000000.0}
	for _, amt := range N  {
		sum1 = 0.0
		for x = 1.0; x <= amt+0.5; x += 1.0  {
			sum1 += 1.0/x	
		}
		sum2 = 0.0
		for x = amt; x > 0.5; x -= 1.0  {
			sum2 += 1.0/x	
		}
		fmt.Printf("%9.0f %22.16e %22.16e %22.16e\n", amt, sum1, sum2, math.Abs(sum1-sum2)/sum2)
	}	
}
